int	ft_sqrt(int nb)
{
	int	i;

	if (nb < 0)
		return (0);
	i = 0;
	while (i <= 46340)
	{
		if (i * i == nb)
			return (i);
		if (i * i > nb)
			break ;
		i++;
	}
	return (0);
}
